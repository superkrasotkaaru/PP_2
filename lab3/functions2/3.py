def get_movies_by_category(category_name, movie_list):
    """
    Function to filter movies by a given category.

    Parameters:
    - category_name (str): The category to filter movies.
    - movie_list (list): List of dictionaries representing movies.

    Returns:
    - list: List of movies under the specified category.
    """
    return [movie for movie in movie_list if movie["category"].lower() == category_name.lower()]

